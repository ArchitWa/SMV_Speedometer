/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  HAL_Init();
  SystemClock_Config();


  MX_GPIO_Init();
  SMV_Speedometer speedo = SMV_Speedometer_new();

  speedo.init(&speedo);

  while (1)
  {
      speedo.setValue(&speedo, counter);   // counter = 0–9999 (represents XX.XX)
      speedo.scan(&speedo);                // call every ~2–5 ms

      counter++;
      if (counter > 9999) counter = 0;

      HAL_Delay(2);
  }
}

